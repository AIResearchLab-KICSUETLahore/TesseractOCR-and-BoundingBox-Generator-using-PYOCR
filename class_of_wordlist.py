class ClassOfWordList:
	# class for creating the ground values having information about the words. There contents and coordinates.
	word_content = None
	X_0 = None
	Y_0 = None
	X_1 = None
	Y_1 = None

	# initiation for creation
	def init(self, x_0, x_1, y_0, y_1):
		self.X_0 = x_0
		self.X_1 = x_1	
		self.Y_0 = y_0
		self.Y_1 = y_1
		return
	# setting functions
	def set_x_0(self, x_0):
		self.X_0 = x_0
	def set_x_0(self, y_0):
		self.Y_0 = y_0

	def set_x_0(self, x_1):
		self.X_1 = x_1

	def set_x_0(self, y_1):
		self.Y_1 = y_1


